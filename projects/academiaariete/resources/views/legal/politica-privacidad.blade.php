          <strong>Cookies.</strong> Las cookies son ficheros de texto de pequeño tamaño que se almacenan en el disco duro o en la memoria del 
          ordenador que accede o visita las páginas de determinados sitios web para registrar su actividad en el sitio web, y lograr que la navegación 
