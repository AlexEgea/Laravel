          <strong>Redes Sociales.</strong> Academia Ariete cuenta con perfiles en algunas de las principales redes sociales, de tal forma que 
          podrá producirse un tratamiento de datos de carácter personal de nuestros seguidores, de personas que aparezcan en las publicaciones 
          que hacemos (por ejemplo, fotografías) y de personas que nos envíen mensajes privados.
        </li>
        <li>
          <strong>Boletines, Newsletters o e-mail.</strong> Academia Ariete podrá poner a disposición de los Usuarios la posibilidad de suscribirse
          a un boletín informativo con respecto a las actividades, publicaciones o ponencias que tenga pensado llevar a cabo. Dicho envío será
          automático y gratuito para el Usuario, quien podrá darse de baja en cualquier momento.
        </li>
        <li>
          <strong>Acciones promocionales.</strong> Aquellos datos que el Usuario consienta en enviar a Academia Ariete por ser de su interés participar en estos actos.
        </li>
        <li>
          <strong>Empleo.</strong> Academia Ariete pone a disposición de las personas interesadas en acceder a un puesto de trabajo una serie de 
          mecanismos de contacto en el sitio web. La persona aspirante a la candidatura de empleo estará dando su consentimiento para que Academia 
          Ariete analice la información remitida en su candidatura, en la entrevista de trabajo, en los perfiles sociales de redes profesionales o 
